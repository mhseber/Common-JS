// Double Equal (==) and Triple Equal (===) in JavaScript


//  ===
console.log(5 === "5");
console.log(5 === 5);
console.log(5 === 4);
console.log(5 === 'seber');
console.log('seber' === 'seber');
console.log(true === true);
console.log(5 === true);
console.log(5 === "5");
console.log(5 === 22);
console.log(22 === 22);

